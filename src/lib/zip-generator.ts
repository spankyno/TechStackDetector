function generateLayout(slug: string, fonts: DetectedFont[], r: AnalysisResult): string {
  const primaryFont = fonts[0]
  const fontImportName = primaryFont?.family.replace(/\s+/g, '_') || ''
  const hasGoogleFont = primaryFont?.isGoogleFont && primaryFont.family

  return `import type { Metadata } from 'next'
${hasGoogleFont ? `import { ${fontImportName.replace(/[^a-zA-Z_]/g, '')} } from 'next/font/google'` : ''}
import './globals.css'

export const metadata: Metadata = {
  title: '${slug}',
  description: 'Cloned with DesignClone AI',
}
${hasGoogleFont ? `
const font = ${fontImportName.replace(/[^a-zA-Z_]/g, '')}({
  subsets: ['latin'],
  weight: [${(primaryFont!.weights || ['400']).map(w => `'${w}'`).join(', ')}],
  variable: '${primaryFont!.cssVariable || '--font-primary'}',
})
` : ''}
export default function RootLayout({
  children,
}: {
  children: React.ReactNode
}) {
  return (
    <html lang="en">
      <body${hasGoogleFont ? ` className={font.variable}` : ''}>{children}</body>
    </html>
  )
}
`
}

function generateGlobalCss(r: AnalysisResult): string {
  const fonts = r.fonts || []
  const imports = fonts
    .filter(f => f.isGoogleFont && f.importUrl)
    .map(f => `@import url('${f.importUrl}');`)
    .join('\n')

  return `${imports}

@tailwind base;
@tailwind components;
@tailwind utilities;

:root {
  --color-primary: ${r.colors?.primary || '#000000'};
  --color-secondary: ${r.colors?.secondary || '#333333'};
  --color-accent: ${r.colors?.accent || '#666666'};
  --color-background: ${r.colors?.background || '#ffffff'};
  --color-surface: ${r.colors?.surface || '#f5f5f5'};
  --color-text: ${r.colors?.text || '#1a1a1a'};
  --color-text-muted: ${r.colors?.textMuted || '#888888'};
  --color-border: ${r.colors?.border || '#e5e5e5'};
  --radius: ${r.effects?.borderRadius || '8px'};
}

* {
  box-sizing: border-box;
  margin: 0;
  padding: 0;
}

body {
  background: var(--color-background);
  color: var(--color-text);
  font-size: ${r.typography?.bodySize || '15px'};
  line-height: ${r.typography?.lineHeight || '1.6'};
  -webkit-font-smoothing: antialiased;
  -moz-osx-font-smoothing: grayscale;
}

h1, h2, h3, h4, h5, h6 {
  letter-spacing: ${r.typography?.letterSpacing || 'normal'};
  font-weight: ${r.typography?.fontWeight?.split('/').pop() || '600'};
}
`
}

function generateButtonComponent(r: AnalysisResult): string {
  return `import { cn } from '@/lib/utils'

interface ButtonProps extends React.ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: 'primary' | 'secondary' | 'ghost'
  size?: 'sm' | 'md' | 'lg'
}

export function Button({
  variant = 'primary',
  size = 'md',
  className,
  children,
  ...props
}: ButtonProps) {
  return (
    <button
      className={cn(
        'inline-flex items-center justify-center font-medium transition-all',
        'rounded-[var(--radius)] cursor-pointer',
        {
          primary: 'bg-primary text-white hover:opacity-90',
          secondary: 'bg-surface text-default border border-border hover:bg-background',
          ghost: 'text-muted hover:text-default',
        }[variant],
        {
          sm: 'text-xs px-3 py-1.5',
          md: 'text-sm px-4 py-2',
          lg: 'text-base px-6 py-3',
        }[size],
        className
      )}
      {...props}
    >
      {children}
    </button>
  )
}
`
}

function generateCardComponent(r: AnalysisResult): string {
  return `import { cn } from '@/lib/utils'

interface CardProps {
  className?: string
  children: React.ReactNode
}

export function Card({ className, children }: CardProps) {
  return (
    <div
      className={cn(
        'bg-surface border border-border rounded-[var(--radius)]',
        '${r.effects?.boxShadow === 'subtle' ? 'shadow-sm' : r.effects?.boxShadow === 'medium' ? 'shadow-md' : ''}',
        className
      )}
    >
      {children}
    </div>
  )
}

export function CardHeader({ className, children }: CardProps) {
  return <div className={cn('p-6 border-b border-border', className)}>{children}</div>
}

export function CardContent({ className, children }: CardProps) {
  return <div className={cn('p-6', className)}>{children}</div>
}
`
}

