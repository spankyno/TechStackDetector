function generateTsConfig(): string {
  return JSON.stringify({
    compilerOptions: {
      target: 'ES2017',
      lib: ['dom', 'dom.iterable', 'esnext'],
      allowJs: true,
      skipLibCheck: true,
      strict: true,
      noEmit: true,
      esModuleInterop: true,
      module: 'esnext',
      moduleResolution: 'bundler',
      resolveJsonModule: true,
      isolatedModules: true,
      jsx: 'preserve',
      incremental: true,
      plugins: [{ name: 'next' }],
      paths: { '@/*': ['./src/*'] },
    },
    include: ['next-env.d.ts', '**/*.ts', '**/*.tsx', '.next/types/**/*.ts'],
    exclude: ['node_modules'],
  }, null, 2)
}

function generateNextConfig(sourceUrl: string): string {
  return `import type { NextConfig } from 'next'

// Generated by DesignClone AI — cloned from ${sourceUrl}
const nextConfig: NextConfig = {
  images: {
    remotePatterns: [{ protocol: 'https', hostname: '**' }],
  },
}

export default nextConfig
`
}

function generateTailwindConfig(r: AnalysisResult): string {
  const c = r.colors
  const fonts = r.fonts || []
  const fontFamilyLines = fonts.length
    ? fonts.map(f => `      ${f.tailwindKey || 'sans'}: ['"${f.family}"', '${f.category || 'sans-serif'}'],`).join('\n')
    : `      sans: ['"${r.typography.primaryFont || 'Inter'}"', 'sans-serif'],`

  return `import type { Config } from 'tailwindcss'

const config: Config = {
  content: ['./src/**/*.{js,ts,jsx,tsx,mdx}'],
  theme: {
    extend: {
      colors: {
        primary: '${c?.primary || '#000000'}',
        secondary: '${c?.secondary || '#333333'}',
        accent: '${c?.accent || '#666666'}',
        background: '${c?.background || '#ffffff'}',
        surface: '${c?.surface || '#f5f5f5'}',
        'text-default': '${c?.text || '#1a1a1a'}',
        'text-muted': '${c?.textMuted || '#888888'}',
        border: '${c?.border || '#e5e5e5'}',
      },
      fontFamily: {
${fontFamilyLines}
      },
      borderRadius: {
        DEFAULT: '${r.effects?.borderRadius || '8px'}',
      },
      maxWidth: {
        container: '${r.spacing?.containerMaxWidth || '1200px'}',
      },
    },
  },
} satisfies Config

export default config
`
}

