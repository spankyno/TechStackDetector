function generateSecurityReport(r: AnalysisResult): string {
  const sec = r.security
  if (!sec) return '# Security Report\n\nNo security data available.'

  const vulns = sec.vulnerabilities || []
  const critical = vulns.filter(v => v.severity === 'critical')
  const high = vulns.filter(v => v.severity === 'high')
  const medium = vulns.filter(v => v.severity === 'medium')
  const low = vulns.filter(v => v.severity === 'low' || v.severity === 'info')

  return `# Security Report — ${r.url}
Generated: ${r.analyzedAt}
Tool: DesignClone AI

## Executive Summary

| Metric | Value |
|--------|-------|
| Overall Score | ${sec.overallScore}/100 (${sec.scoreLabel}) |
| Risk Level | ${sec.riskLevel} |
| Total Vulnerabilities | ${vulns.length} |
| Critical | ${critical.length} |
| High | ${high.length} |
| Medium | ${medium.length} |
| Low / Info | ${low.length} |

## SSL / TLS Configuration

| Property | Value |
|----------|-------|
| Grade | ${sec.ssl?.grade || '—'} |
| TLS Version | ${sec.ssl?.tlsVersion || '—'} |
| HSTS | ${sec.ssl?.hsts ? '✓ Enabled' : '✗ Missing'} |
| HSTS max-age | ${sec.ssl?.hstsMaxAge || '—'} |
| Certificate Provider | ${sec.ssl?.provider || '—'} |

## HTTP Security Headers

| Header | Status |
|--------|--------|
| Content-Security-Policy | ${sec.headers?.contentSecurityPolicy || '—'} |
| Strict-Transport-Security | ${sec.headers?.strictTransportSecurity || '—'} |
| X-Frame-Options | ${sec.headers?.xFrameOptions || '—'} |
| X-Content-Type-Options | ${sec.headers?.xContentTypeOptions || '—'} |
| Referrer-Policy | ${sec.headers?.referrerPolicy || '—'} |
| Permissions-Policy | ${sec.headers?.permissionsPolicy || '—'} |
| Cross-Origin-Opener-Policy | ${sec.headers?.crossOriginOpenerPolicy || '—'} |
| Cross-Origin-Resource-Policy | ${sec.headers?.crossOriginResourcePolicy || '—'} |

## Protection Mechanisms

- **Rate Limiting:** ${sec.rateLimit}
- **Bot Protection:** ${sec.botProtection}
- **DDoS Protection:** ${sec.ddosProtection}

## Vulnerabilities

${vulns.map(v => `### [${v.severity.toUpperCase()}] ${v.id} — ${v.title}

| Field | Value |
|-------|-------|
| Category | ${v.category} |
| CVSS Score | ${v.cvss} |
| CWE | ${v.cwe} |
| Affected | ${v.affected} |

**Description:**  
${v.description}

**Evidence:**  
${v.evidence || '—'}

**Recommended Fix:**  
${v.solution}

**References:** ${(v.references || []).join(', ') || '—'}

---`).join('\n\n')}

## Priority Recommendations

${(sec.recommendations || []).map((r, i) => `${i + 1}. ${r}`).join('\n')}
`
}

