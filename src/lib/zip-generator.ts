// src/lib/zip-generator.ts
// Generates a real Next.js 15 project ZIP from analysis results

import type { AnalysisResult, DetectedFont } from '@/types'

export async function generateProjectZip(result: AnalysisResult): Promise<Buffer> {
  const JSZip = (await import('jszip')).default
  const zip = new JSZip()

  const slug = result.url
    .replace(/https?:\/\//, '')
    .split('/')[0]
    .replace(/[^a-z0-9]/gi, '-')
    .toLowerCase()

  const root = zip.folder(`${slug}-clone`)!
  const src = root.folder('src')!
  const app = src.folder('app')!
  const components = src.folder('components')!
  const lib = src.folder('lib')!
  const pub = root.folder('public')!

  const fonts = result.fonts || []
  const c = result.colors
  const t = result.typography

  // ── Root files ──────────────────────────────────────────────────────────────

  root.file('package.json', generatePackageJson(slug))
  root.file('tsconfig.json', generateTsConfig())
  root.file('next.config.ts', generateNextConfig(result.url))
  root.file('tailwind.config.ts', generateTailwindConfig(result))
  root.file('postcss.config.mjs', `const config = { plugins: { '@tailwindcss/postcss': {} } }\nexport default config`)
  root.file('.env.example', generateEnvExample(result.url))
  root.file('.gitignore', generateGitignore())
  root.file('README.md', generateReadme(slug, result))

  // ── App directory ───────────────────────────────────────────────────────────

  app.file('layout.tsx', generateLayout(slug, fonts, result))
  app.file('globals.css', generateGlobalCss(result))
  app.file('page.tsx', result.nextjsCode || generateFallbackPage(result))

  // ── Components ──────────────────────────────────────────────────────────────

  components.file('ui/button.tsx', generateButtonComponent(result))
  components.file('ui/card.tsx', generateCardComponent(result))

  // ── Lib ─────────────────────────────────────────────────────────────────────

  lib.file('utils.ts', `import { clsx, type ClassValue } from 'clsx'\nimport { twMerge } from 'tailwind-merge'\n\nexport function cn(...inputs: ClassValue[]) {\n  return twMerge(clsx(inputs))\n}`)

  // ── Public ──────────────────────────────────────────────────────────────────

  pub.file('index.html', result.htmlCode || '')

  // ── Design system files ─────────────────────────────────────────────────────

  root.file('design-tokens.json', JSON.stringify({
    generatedAt: result.analyzedAt,
    source: result.url,
    framework: result.framework,
    style: result.style,
    styleNotes: result.styleNotes,
    colors: result.colors,
    typography: result.typography,
    spacing: result.spacing,
    effects: result.effects,
    fonts: result.fonts,
    components: result.components,
  }, null, 2))

  root.file('infrastructure.json', JSON.stringify(result.infrastructure || {}, null, 2))

  root.file('security-report.md', generateSecurityReport(result))

  root.file('VIBE_PROMPT.md', `# Vibe Coding Prompt\n## Source: ${result.url}\n## Generated: ${result.analyzedAt}\n\n---\n\n${result.vibePrompt}`)

  return zip.generateAsync({
    type: 'nodebuffer',
    compression: 'DEFLATE',
    compressionOptions: { level: 6 },
  })
}

